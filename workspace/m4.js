function show(){
    var myElement = document.getElementById("mydiv");
    var myButton = document.getElementById("mybtn");
    myButton.value="隐藏";
    myElement.style.display = "block";
}

function hide(){
    var myElement = document.getElementById("mydiv");
    var myButton = document.getElementById("mybtn");
    myButton.value="显示";
    myElement.style.display = "none";
}
    

